const words = [];
let wordIndex = 0;

for (const category of categories) {
  for (let clusterIdx = 0; clusterIdx < category.clusters.length; clusterIdx++) {
    const cluster = category.clusters[clusterIdx];
    const clusterDifficulty = clusterIdx < 2 ? 'easy' : clusterIdx < 4 ? 'medium' : 'hard';
    for (const item of cluster.words) {
      const related = cluster.words.filter((word) => word !== item).slice(0, 4);
      const difficulty = clusterDifficulty;
      const hints = cluster.hints && cluster.hints.length > 0 ? cluster.hints : category.hints;

      words.push({
        id: `word_${wordIndex.toString().padStart(5, '0')}`,
        text_en: item,
        text_ar: item,
        category: category.key,
        difficulty,
        hints,
        decoys: related,
      });

      wordIndex += 1;
    }
  }
}

